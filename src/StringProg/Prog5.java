package StringProg;

public class Prog5 {

	public static void main(String[] args)
	{
		String str1 = "Core Java";
//charAt is use in which have place the which alphabet.		 
		System.out.println(str1.charAt(0));//C
		System.out.println(str1.charAt(3));//e
		System.out.println(str1.charAt(5));//J
		System.out.println(str1.charAt(4));//space
        System.out.println(str1.charAt(8));//a
        System.out.println(str1.indexOf("J"));//5
	}

}
